    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        nama = NamaBarang.getText();
        merek = MerekBarang.getText();
        jenis = "";
        if (PecahBelah.isSelected()){
            jenis = PecahBelah.getText();
        }
        else if (Normal.isSelected()){
            jenis = Normal.getText();
        }
        else{
           jenis = "belum memilih";
        }
        panjang = PanjangBarang.getText();
        lebar = LebarBarang.getText();
        tanggalmasuk = TanggalMasuk.getText();
        tujuan = TujuanBarang.getText();
        tanggalkeluar = TanggalKeluar.getText();
        
        
        try{
            sql = "INSERT INTO daftar_barang VALUES (?,?,?,?,?,?,?,?,?)";
            ps = conn.prepareStatement(sql);
            
            ps.setInt(1,0);
            ps.setString(2, nama);
            ps.setString(3, merek);
            ps.setString(4, jenis);
            ps.setString(5, panjang);
            ps.setString(6, lebar);
            ps.setString(7, tanggalmasuk);
            ps.setString(8, tujuan);
            ps.setString(9, tanggalkeluar);
            ps.setInt(1,0);
            
            
                //validasi untuk memasukan data
                if (NamaBarang.getText().equals("")) {
                     JOptionPane.showMessageDialog(null, "nama tidak boleh kosong", "Peringatan", JOptionPane.WARNING_MESSAGE);
                } else if (MerekBarang.getText().equals("")) {
                     JOptionPane.showMessageDialog(null, "merek tidak boleh kosong", "Peringatan", JOptionPane.WARNING_MESSAGE);
                } else if (PecahBelah.isSelected() == false && Normal.isSelected() == false ) {
                     JOptionPane.showMessageDialog(null, "jenis tidak boleh kosong", "Peringatan", JOptionPane.WARNING_MESSAGE);
                }else if (PanjangBarang.getText().equals("")) {
                     JOptionPane.showMessageDialog(null, "panjang tidak boleh kosong", "Peringatan", JOptionPane.WARNING_MESSAGE);
                }else if (LebarBarang.getText().equals("")) {
                     JOptionPane.showMessageDialog(null, "lebar tidak boleh kosong", "Peringatan", JOptionPane.WARNING_MESSAGE);
                }else if (TanggalMasuk.getText().equals("")) {
                     JOptionPane.showMessageDialog(null, "tanggal masuk tidak boleh kosong", "Peringatan", JOptionPane.WARNING_MESSAGE);
                }else if (TujuanBarang.getText().equals("")) {
                     JOptionPane.showMessageDialog(null, "tujuan tidak boleh kosong", "Peringatan", JOptionPane.WARNING_MESSAGE);
                }else if (TanggalKeluar.getText().equals("")) {
                     JOptionPane.showMessageDialog(null, "tanggal keluar tidak boleh kosong", "Peringatan", JOptionPane.WARNING_MESSAGE);
                }else{
                    int rt = ps.executeUpdate();
                 JOptionPane.showMessageDialog(null,"Data berhasil disimpan", "information", JOptionPane.INFORMATION_MESSAGE );
                }

            
        }catch (Exception e) {
            System.out.println("error : "+ e.getMessage());
        } 
    }//GEN-LAST:event_jButton1ActionPerformed

    
