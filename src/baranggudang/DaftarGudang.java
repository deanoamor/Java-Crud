    private void ShowBarangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ShowBarangActionPerformed
        // TODO add your handling code here:
        try{
             String query = "SELECT id_barang,nama_barang,merek_barang,jenis_barang,panjang_barang,lebar_barang,tanggal_masuk,tujuan_barang,tanggal_keluar FROM daftar_barang";
             st = conn.createStatement();
             rs = st.executeQuery(query);
             DefaultTableModel tm = (DefaultTableModel) TableBarang.getModel();
             tm.setRowCount(0);
             
             while(rs.next()){
//                 System.out.println("id siswa: "+rs.getString(1)+"\n"+"nama siswa: "+rs.getString(2)+"\n");
                 Object barang[] = {rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6),rs.getString(7),rs.getString(8),rs.getString(9)};
                 tm.addRow(barang);
             }
            
        }catch (Exception e) {
            System.out.println("error : "+ e.getMessage());
        } 
    }//GEN-LAST:event_ShowBarangActionPerformed
    
    
